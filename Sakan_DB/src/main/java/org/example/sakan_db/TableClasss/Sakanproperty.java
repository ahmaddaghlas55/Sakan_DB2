package com.sample;


public class Sakanproperty {

  private long propertyId;
  private String propertytype;
  private long numberofrooms;
  private long propertyprice;
  private long propertyphonenumber;
  private String propertycity;
  private String propertycountry;
  private long ownerId;
  private long adminId;


  public long getPropertyId() {
    return propertyId;
  }

  public void setPropertyId(long propertyId) {
    this.propertyId = propertyId;
  }


  public String getPropertytype() {
    return propertytype;
  }

  public void setPropertytype(String propertytype) {
    this.propertytype = propertytype;
  }


  public long getNumberofrooms() {
    return numberofrooms;
  }

  public void setNumberofrooms(long numberofrooms) {
    this.numberofrooms = numberofrooms;
  }


  public long getPropertyprice() {
    return propertyprice;
  }

  public void setPropertyprice(long propertyprice) {
    this.propertyprice = propertyprice;
  }


  public long getPropertyphonenumber() {
    return propertyphonenumber;
  }

  public void setPropertyphonenumber(long propertyphonenumber) {
    this.propertyphonenumber = propertyphonenumber;
  }


  public String getPropertycity() {
    return propertycity;
  }

  public void setPropertycity(String propertycity) {
    this.propertycity = propertycity;
  }


  public String getPropertycountry() {
    return propertycountry;
  }

  public void setPropertycountry(String propertycountry) {
    this.propertycountry = propertycountry;
  }


  public long getOwnerId() {
    return ownerId;
  }

  public void setOwnerId(long ownerId) {
    this.ownerId = ownerId;
  }


  public long getAdminId() {
    return adminId;
  }

  public void setAdminId(long adminId) {
    this.adminId = adminId;
  }

}
